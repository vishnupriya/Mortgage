import UIKit

extension UIFont {
    
    class func smallFont() -> UIFont {
        return UIFont(name:"Cochin", size:16)!
        
    }
    class func normalFont() -> UIFont {
        return UIFont(name:"Cochin", size:18)!
 
    }

    class func boldFont() -> UIFont {
        return UIFont(name:"Cochin-Bold", size:20)!
    }
}
